var color = "blue";

switch (color) {
    case "blue":
        console.log("color is blue");
        break;
    case "red":
        console.log("color is red");
        break;
    case "white":
        console.log("color is white");
        break;
    case "gray":
        console.log("color is gray");
        break;
    case "navyblue":
        console.log("color is navyblue");
        break;
    case "cyan":
        console.log("color is cyan");
        break;

    default:
        console.log("unknown color!!!")
        break;
}